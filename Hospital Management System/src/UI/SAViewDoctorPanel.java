    private void btnViewDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewDoctorActionPerformed
         int selectedRowIndex = tblDcotorView.getSelectedRow();
        
        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please Select a row to Update");
            return;
        }
        
         DefaultTableModel model = (DefaultTableModel) tblDcotorView.getModel();
        Doctor selectedDoctor = (Doctor) model.getValueAt(selectedRowIndex, 0);
        
        
        txtName.setText(selectedDoctor.getName());
        txtPassword.setText(selectedDoctor.getPassword());
        txtCity.setText(selectedDoctor.getAddress().getCity().getCityName());
        txtCommunity.setText(selectedDoctor.getAddress().getCommunity().getCommunityName());
        txtHouse.setText(selectedDoctor.getAddress().getHouse().getHouseName());
        txtDateofBirth.setDate(selectedDoctor.getDob());
        txtPhone.setText(String.valueOf(selectedDoctor.getPhone()));
        txtGender.setText(selectedDoctor.getGender());
        
        
        txtHospitalName.setText(selectedDoctor.getHospital().getHospitalName());
        txtCommunityName.setText(selectedDoctor.getHospital().getHospitalCommunity());
        txtHospitalCityName.setText(selectedDoctor.getHospital().getHospitalCity());
        
        
        
    }//GEN-LAST:event_btnViewDoctorActionPerformed

    private void btnUpdateDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateDoctorActionPerformed
      
        
        
       int selectedRowIndex = tblDcotorView.getSelectedRow();
       DefaultTableModel model = (DefaultTableModel) tblDcotorView.getModel();
       Doctor d = (Doctor) model.getValueAt(selectedRowIndex, 0); 
       
        d.setName(txtName.getText());
       d.getAddress().getCity().setCityName(txtCity.getText());
       d.getAddress().getCommunity().setCommunityName(txtCommunity.getText());
       d.getAddress().getHouse().setHouseName(txtHouse.getText());
       d.setDob(txtDateofBirth.getDate());
       d.setGender(txtGender.getText());
       d.setPhone(Long.parseLong(txtPhone.getText()));
       
       d.getHospital().setHospitalCity(txtHospitalCityName.getText());
       d.getHospital().setHospitalCommunity(txtCommunityName.getText());
       d.getHospital().setHospitalName(txtHospitalName.getText());
       
       JOptionPane.showMessageDialog(this, "Doctor information is updated.");
       
       poputlateDoctorTable();
       
       
        txtName.setText("");
        txtPassword.setText("");
        txtCity.setText("");
        txtCommunity.setText("");
        txtHouse.setText("");
        txtDateofBirth.setDate(null);
        txtPhone.setText("");
        txtGender.setText("");
        
        
        txtHospitalName.setText("");
        txtCommunityName.setText("");
        txtHospitalCityName.setText("");
       
       
       
       
    }//GEN-LAST:event_btnUpdateDoctorActionPerformed

