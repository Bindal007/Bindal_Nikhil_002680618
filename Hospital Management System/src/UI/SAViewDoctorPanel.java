    private void poputlateDoctorTable() {
        
        
          DefaultTableModel model = (DefaultTableModel) tblDcotorView.getModel();
                  
                model.setRowCount(0);
                
                for(Doctor d: doctorDirectory.getDoctorDirectory())
                {
                    Object[] row = new Object[7];
                    row[0]=d;
                    row[1]=d.getAddress().getCity().getCityName();
                    row[2]=d.getAddress().getCommunity().getCommunityName();
                    row[3]=d.getAddress().getHouse().getHouseName();
                    row[4]=d.getDob();
                    row[5]=d.getPhone();
                    row[6]=d.getHospital().getHospitalName();
                    
                    
                    
                    model.addRow(row);
                    
                    
                }
    
    }
