    private void btnDeleteDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteDoctorActionPerformed
        int selectedRowIndex = tblDcotorView.getSelectedRow();
        
        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please Select a row to delete");
            return;
        }
        
        DefaultTableModel model = (DefaultTableModel) tblDcotorView.getModel();
        Doctor selectedDoctor = (Doctor) model.getValueAt(selectedRowIndex, 0);
        
       doctorDirectory.removeDoctor(selectedDoctor);
        
        JOptionPane.showMessageDialog(this, "Doctor is Deleted");
        
        poputlateDoctorTable();
        
    }//GEN-LAST:event_btnDeleteDoctorActionPerformed


